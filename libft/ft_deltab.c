#include "libft.h"

void	ft_deltab(char ***double_tab)
{
	int i;

	i = 0;
	if (double_tab)
	{
		while (double_tab[0][i])
		{
			free(double_tab[0][i]);
			i++;
		}
		free(double_tab[0]);
	}
	return ;
}
