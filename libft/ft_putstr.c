#include "libft.h"

static	int	len(const char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i += 1;
	return (i);
}

void		ft_putstr(const char *str)
{
	if (str)
		write(1, str, len(str));
}
