#include "libft.h"

size_t	ft_strcspn(const char *s, const char *charset)
{
	size_t	i;

	i = 0;
	while (s[i] != '\0' && (ft_strchr(charset, s[i])) == 0)
	{
		i++;
	}
	return (i);
}
